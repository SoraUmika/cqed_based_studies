"""
Phase 1 — Analytical Theory and Steady-State Analysis
======================================================

Goals:
  1. Derive and map the steady-state pointer-state separation |Δα_ss| vs χ/κ.
    2. Show the optimal drive frequency is ω_d = ω_r + χ/2, equivalently Δ_g = −χ/2.
  3. Compute SNR² vs κT in steady-state and compare against time-resolved simulation.
  4. Validate analytical results against cqed_sim.ReadoutResonator (cross-check).
  5. Compare endpoint-separation and integrated-SNR² objectives to motivate Phase 2.
  6. Quantify the steady-state photon number and measurement rate γ_meas.

Outputs:
  data/phase1_results.npz  — all numerical arrays
  (figures generated by plot_results.py)
"""

